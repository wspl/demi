import type { BundledTheme } from 'shiki'

export interface CodeThemePalette {
  // Editor UI
  background: string
  foreground: string
  selection: string
  lineHighlight: string
  cursor: string
  lineNumber: string
  activeLineNumber: string
  // Syntax highlighting
  keyword: string
  controlKeyword: string
  string: string
  number: string
  comment: string
  function: string
  variable: string
  type: string
  constant: string
  operator: string
  punctuation: string
  tag: string
  attribute: string
  regexp: string
  invalid: string
}

export interface CodeThemeDefinition {
  id: string
  label: string
  dark: {
    palette: CodeThemePalette
    shikiTheme: BundledTheme
  }
  light: {
    palette: CodeThemePalette
    shikiTheme: BundledTheme
  }
}

